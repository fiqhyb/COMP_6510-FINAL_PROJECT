    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        if (deposit == true)
            jToggleButton1.doClick();
        if (withdraw == true)
            jToggleButton2.doClick(); 
        
        if(!jTextField1.getText().equals("")){
            int transferacc = Integer.parseInt(JOptionPane.showInputDialog("Enter the account number to transfer:"));
            try {
                java.sql.Connection con=DriverManager.getConnection("jdbc:derby://localhost:1527/AccountDatabase", "hex123", "hex123");

                String DBQ="SELECT * FROM hex123.ACCOUNT WHERE ACCNUMBER= "+transferacc+" ";
                java.sql.Statement stmt=con.createStatement();
                ResultSet rs=stmt.executeQuery(DBQ);
                if(rs.next()){
                    String transfername = rs.getString("USERNAME");
                    double targetblc = rs.getDouble("BALANCE");
                    double newtargetblc = targetblc + Double.parseDouble(jTextField1.getText());

                    if (balance >= Double.parseDouble(jTextField1.getText()) && balance - Double.parseDouble(jTextField1.getText()) >= 50000) {
                        String DBQT="UPDATE hex123.ACCOUNT SET BALANCE= "+newtargetblc+" WHERE ACCNUMBER= "+transferacc+" ";
                        java.sql.PreparedStatement pstmt=con.prepareStatement(DBQT);
                        pstmt.executeUpdate();
                        balance = balance - Double.parseDouble(jTextField1.getText());
                        jLabel5.setText("Rp "+NumberFormat.getNumberInstance(Locale.GERMAN).format(balance));
                        jLabel9.setText("You transfered "+"Rp "+NumberFormat.getNumberInstance(Locale.GERMAN).format(Double.parseDouble(jTextField1.getText()))+" to "+transfername);
                    }
                    else
                        jLabel9.setText("REMAINING BALANCE CAN NOT BE UNDER 50.000");
                }
            }
            catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        }
        else
            jLabel9.setText("SET THE AMOUNT TO TRANSFER");
    }//GEN-LAST:event_jButton15ActionPerformed

