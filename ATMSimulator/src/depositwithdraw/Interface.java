    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        if(deposit == true){
            balance = balance + Double.parseDouble(jTextField1.getText());
            jLabel5.setText("Rp "+NumberFormat.getNumberInstance(Locale.GERMAN).format(balance));
            jLabel9.setText("You deposited "+"Rp "+NumberFormat.getNumberInstance(Locale.GERMAN).format(Double.parseDouble(jTextField1.getText())));
            System.out.println(balance);
        }
        else if(withdraw == true && balance >= Double.parseDouble(jTextField1.getText()) && balance - Double.parseDouble(jTextField1.getText()) >= 50000){
            balance = balance - Double.parseDouble(jTextField1.getText());
            jLabel5.setText("Rp "+NumberFormat.getNumberInstance(Locale.GERMAN).format(balance));
            jLabel9.setText("You withdrawn "+"Rp "+NumberFormat.getNumberInstance(Locale.GERMAN).format(Double.parseDouble(jTextField1.getText())));
        }
        else {
            if(withdraw == true)
                jLabel9.setText("REMAINING BALANCE CAN NOT BE UNDER 50.000");
            else
                jLabel9.setText("PLEASE SELECT AN ACTION TO PERFORM");
        }
    }//GEN-LAST:event_jButton12ActionPerformed

